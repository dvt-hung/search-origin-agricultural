package com.example.apptxng.model;

public class ImageHistory {
    private int idImageHistory;
    private String imageHistory;
    private String idHistory;

    public int getIdImageHistory() {
        return idImageHistory;
    }

    public void setIdImageHistory(int idImageHistory) {
        this.idImageHistory = idImageHistory;
    }

    public String getImageHistory() {
        return imageHistory;
    }

    public void setImageHistory(String imageHistory) {
        this.imageHistory = imageHistory;
    }

    public String getIdHistory() {
        return idHistory;
    }

    public void setIdHistory(String idHistory) {
        this.idHistory = idHistory;
    }
}
